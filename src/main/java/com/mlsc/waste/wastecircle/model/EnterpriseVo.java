package com.mlsc.waste.wastecircle.model;

public class EnterpriseVo {
	private String entId;
	private String entName;
	private String entAddress;
	private String businessCode;
	private String fileId;
	private String entTypeCode;
	private String entTypeValue;
	public String getEntId() {
		return entId;
	}
	public void setEntId(String entId) {
		this.entId = entId;
	}
	public String getEntName() {
		return entName;
	}
	public void setEntName(String entName) {
		this.entName = entName;
	}
	public String getEntAddress() {
		return entAddress;
	}
	public void setEntAddress(String entAddress) {
		this.entAddress = entAddress;
	}
	public String getBusinessCode() {
		return businessCode;
	}
	public void setBusinessCode(String businessCode) {
		this.businessCode = businessCode;
	}
	public String getFileId() {
		return fileId;
	}
	public void setFileId(String fileId) {
		this.fileId = fileId;
	}
	public String getEntTypeCode() {
		return entTypeCode;
	}
	public void setEntTypeCode(String entTypeCode) {
		this.entTypeCode = entTypeCode;
	}
	public String getEntTypeValue() {
		return entTypeValue;
	}
	public void setEntTypeValue(String entTypeValue) {
		this.entTypeValue = entTypeValue;
	}
	
}
